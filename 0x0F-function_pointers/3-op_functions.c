#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - returns the sum
 *
 * @a: integer one
 * @b: integer two
 *
 * Return: 0 (always)
 */

int op_add(int a, int b)
{
	return (a + b);
return (0);
}

/**
 * op_sub - returns the difference
 *
 * @a: integer one
 * @b: integer two
 *
 * Return: 0 (always)
 */

int op_sub(int a, int b)
{
	return (a - b);
return (0);
}

/**
 * op_mul - returns the product
 *
 * @a: integer one
 * @b: integer two
 *
 * Return: 0 (always)
 */

int op_mul(int a, int b)
{
	return (a * b);
return (0);
}

/**
 * op_div - returns the result of division
 *
 * @a: integer one
 * @b: integer two
 *
 * Return: 0 (always)
 */

int op_div(int a, int b)
{
	return (a / b);
return (0);
}

/**
 * op_mod - returns the remainder
 *
 * @a: integer one
 * @b: integer two
 *
 * Return: 0 (always)
 */

int op_mod(int a, int b)
{
	return (a % b);
return (0);
}

